package students.file;

public class Student {
 private String name;
 private int math;
 private int eng;
 private int kor;


    public void setName(String name){
        this.name=name;
    }
    public void setMath(int math){
        this.math=math;
    }
    public void setEng(int eng){
        this.eng=eng;
    }
    public void setKor(int kor){
        this.kor = kor;
    }
    public String getName(){
        return name;
    }
    public int getMath(){
        return math;
    }
    public int getEng(){
        return eng;
    }
    public int getKor(){
        return kor;
    }





    }

